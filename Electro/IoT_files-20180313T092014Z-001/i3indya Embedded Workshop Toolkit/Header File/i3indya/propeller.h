void interrupt_init()
{
MCUCR |=(1<<ISC01);
GICR |=(1<<INT0);
}

void timer_init()
{
TCCR0 |=(1<<CS01);
TIMSK |=(1<<TOIE0);
TCNT0=235;
}

void colon()
{
for(int l=0;l<2;l++)
{
PORTB=col[l];
_delay_us(100);
}
PORTB=0;
_delay_us(500);
}

void num(int n)
{
char d=0;
d=n/10;
propeller(d);
d=n%10;
propeller(d);
}

void propeller(char p)
{
for(int i=0;i<8;i++)
{
PORTB=number[(p * 8) + i];
_delay_us(400);
}
PORTB=0;
_delay_us(500);
}



