void display_char(unsigned char c)
{
for(int i=0;i<8;i++)
{
PORTB=alphabet[((c-65)*8)+i];
_delay_us(400);
}
PORTB=0;
_delay_us(500);
}

void display_string(unsigned char *s)
{
int j=0;
while (s[j]!='\0')
{
display_char(s[j]);
j++;
}
}

